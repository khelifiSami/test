package com.formation.adhesion.ods.web.shared.data;

import java.io.Serializable;
import java.util.Date;
 
public class Event implements Serializable {
	private String type;
	private long id;
	private Date date;
	public Event() {
		
	}
	public Event(long id, String type, Date date) {
		super();
		this.type = type;
		this.id = id;
		this.date = date;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

}